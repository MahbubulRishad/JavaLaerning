package java_basic.c02;

public class UnaryOperatorTest {
    public static void main(String[] args) {
        int num=25;

        System.out.println(num);
        System.out.println(num++);
        System.out.println(++num);
        System.out.println(num);
        System.out.println(--num);
        System.out.println(--num);
        System.out.println(num);

        num = 13+13;
        System.out.println("-------------");
        System.out.println(num);
        System.out.println(num++);
        System.out.println(++num);
        System.out.println(num);
        System.out.println(--num);
        System.out.println(--num);
        System.out.println(num);
    }
}
